const mongoose = require("mongoose");

const PostSchema = mongoose.Schema({
  //describes how posts data is going to look
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Posts", PostSchema);
